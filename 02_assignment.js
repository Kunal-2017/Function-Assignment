// Create a JavaScript function called generateGreeting that takes a name as an argument and returns a
// personalized greeting message. Use this function to greet three different people.

// Function to generate personalized greeting
function generateGreeting(name) {
    return `Hello, ${name}! Welcome to our website.`;
}

// Example usage
const person1 = "Alice";
const person2 = "Bob";
const person3 = "Charlie";

console.log(generateGreeting(person1)); // Greet Alice
console.log(generateGreeting(person2)); // Greet Bob
console.log(generateGreeting(person3)); // Greet Charlie
